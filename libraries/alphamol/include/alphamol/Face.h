/*********************************************************************************
 *	The Face class
 *********************************************************************************/

#ifndef FACE_H
#define FACE_H

/*********************************************************************************
  Edge class
 *********************************************************************************/

  class Face {
	public:
		int Vertices[3];
		int Edges[3];
		double gamma;

		Face() {
		}

		Face(int i, int j, int k, int e1, int e2, int e3, double S);

		~Face();
  };

/*********************************************************************************
  Constructor
 *********************************************************************************/

  Face::Face(int i, int j, int k, int e1, int e2, int e3, double S) {

	Vertices[0] = i;
	Vertices[1] = j;
	Vertices[2] = k;
	Edges[0] = e1;
	Edges[1] = e2;
	Edges[2] = e3;
	gamma = S;
  }

/*********************************************************************************
  Destructor
 *********************************************************************************/

  Face::~Face() {
  }

#endif // FACE_H
