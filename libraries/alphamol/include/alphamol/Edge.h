/*********************************************************************************
 *	The Edge class
 *********************************************************************************/

#ifndef EDGE_H
#define EDGE_H

/*********************************************************************************
  Edge class
 *********************************************************************************/

  class Edge {
	public:
		int Vertices[2];
		double gamma;
		double sigma;
		double Length, Surf, Vol; 
		double CoefM1, CoefM2, CoefG1, CoefG2;
		double dsurf, dvol, dmean, dgauss;

		Edge() {
		}

		Edge(int i, int j);

		~Edge();
  };

/*********************************************************************************
  Constructor
 *********************************************************************************/

  Edge::Edge(int i, int j) {

	Vertices[0] = i;
	Vertices[1] = j;
	gamma = 0.0;
	sigma = 0.0;
  }

/*********************************************************************************
  Destructor
 *********************************************************************************/

  Edge::~Edge() {
  }

#endif // EDGE_H
